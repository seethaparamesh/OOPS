
public class printingNumbers {

	public static void main(String[] args) {
		
 for(int i =1;i<6;i++){ //printing pattern 1
	 for(int j=1;j<=i;j++){
		 System.out.print(j+" ");
	 }System.out.println();
	 
	 }System.out.println();
 
 for(int k=1;k<6;k++){ // printing pattern 2
	 for(int l=1;l<=k;l++){
		 System.out.print(k+ " ");
	 }System.out.println();
 }System.out.println();
 
 
 
 
 for(int k=1;k<6;k++){ // printing pattern 2
	 for(int l=1;l<=k;l++){
		 System.out.print( "*");
	 }System.out.println();
 }
 
 }
	}


